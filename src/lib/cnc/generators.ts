/**
 * Generate Heidenhain TNC 640 code
 */
export function generateHeidenhainTNC640(program: CNCProgram, options?: ConversionOptions): string {
  const lines: string[] = [];

  for (let i = 0; i < program.blocks.length; i++) {
    const block = program.blocks[i];
    let line = "";

    if (block.isProgramStart) {
      lines.push(`BEGIN PGM ${program.programName || "PROGRAM"} MM`);
      lines.push(`;Generated by CNC Converter`);
      continue;
    }

    if (block.isProgramEnd) {
      lines.push(`END PGM ${program.programName || "PROGRAM"} MM`);
      continue;
    }

    if (block.heidenhainCommand) {
      if (block.heidenhainCommand === "L") {
        line = "L ";
        for (const axis of ["X", "Y", "Z", "A", "B", "C"]) {
          if (block.axes[axis] !== undefined) {
            const val = block.axes[axis];
            line += `${axis}${val >= 0 ? "+" : ""}${formatNumber(val)} `;
          }
        }
        if (block.feed) line += `F${Math.round(block.feed)} `;
        if (block.spindleSpeed) line += `S${Math.round(block.spindleSpeed)} `;
        for (const m of block.mCodes) line += m + " ";
        lines.push(line.trim());
        continue;
      }

      if (block.heidenhainCommand === "TOOL CALL") {
        line = `TOOL CALL ${block.toolNumber || 1} Z S${Math.round(block.spindleSpeed || 5000)}`;
        lines.push(line);
        continue;
      }

      if (block.heidenhainCommand === "CYCL DEF") {
        const operation = block.mCodes[0] || "DRILLING";
        line = `CYCL DEF ${block.cycle || "200"} ${operation}`;
        lines.push(line);
        continue;
      }

      if (block.heidenhainCommand === "CYCL CALL") {
        lines.push("CYCL CALL");
        continue;
      }

      if (block.heidenhainCommand === "BEGIN") {
        lines.push(`BEGIN PGM ${program.programName || "PROGRAM"} MM`);
        continue;
      }

      if (block.heidenhainCommand === "END") {
        lines.push(`END PGM ${program.programName || "PROGRAM"} MM`);
        continue;
      }

      if (block.heidenhainCommand === "LBL") {
        lines.push(block.raw);
        continue;
      }

      if (["CC", "CP", "CR", "CT", "APPR", "DEP", "RND", "CHF", "MIRROR", "ROT", "SCALE", "TRAFO", "BLK FORM", "FN0", "FN1", "FN2", "Q"].includes(block.heidenhainCommand)) {
        lines.push(block.raw);
        continue;
      }
    }

    // Standard G-code to Heidenhain conversion
    if (block.gCodes.length > 0) {
      // Convert G0/G1 to L moves
      const gNum = parseInt(block.gCodes[0].replace("G", ""), 10);
      if (!isNaN(gNum) && (gNum === 0 || gNum === 1)) {
        line = "L ";
        for (const axis of ["X", "Y", "Z", "A", "B", "C"]) {
          if (block.axes[axis] !== undefined) {
            line += `${axis}${block.axes[axis] >= 0 ? "+" : ""}${formatNumber(block.axes[axis])} `;
          }
        }
        if (gNum === 0) line += "R0 ";
        if (block.feed) line += `F${Math.round(block.feed)} `;
        if (block.spindleSpeed) line += `S${Math.round(block.spindleSpeed)} `;
        for (const m of block.mCodes) line += m + " ";

        // Cycle detection from G-codes
        if (gNum >= 81 && gNum <= 89) {
          const heidenhainCycles: Record<number, string> = {
            81: "CYCL DEF 200 DRILLING", 82: "CYCL DEF 201 DWELLING",
            83: "CYCL DEF 202 PECKING", 84: "CYCL DEF 203 TAPPING",
          };
          lines.push(heidenhainCycles[gNum] || `CYCL DEF 200 DRILLING`);
          if (block.axes["Z"] !== undefined) {
            // Add Q parameters for the cycle
            lines.push(`QL200=${block.addresses["R"] || 2}; SET-UP`);
            lines.push(`QL201=${-(Math.abs(block.axes["Z"]))}; DEPTH`);
            lines.push(`QL202=2; FEED`);
          }
          lines.push("CYCL CALL");
          continue;
        }

        lines.push(line.trim());
        continue;
      }

      // G02/G03 → CR/CP
      if (!isNaN(gNum) && (gNum === 2 || gNum === 3)) {
        const dir = gNum === 2 ? "DR-" : "DR+";
        line = "CR ";
        for (const axis of ["X", "Y", "Z"]) {
          if (block.axes[axis] !== undefined) {
            line += `${axis}${block.axes[axis] >= 0 ? "+" : ""}${formatNumber(block.axes[axis])} `;
          }
        }
        if (block.addresses["I"] !== undefined && block.addresses["J"] !== undefined) {
          const iVal = block.addresses["I"] as number;
          const jVal = block.addresses["J"] as number;
          const r = Math.sqrt(iVal * iVal + jVal * jVal);
          line += `${dir} R+${formatNumber(r)} `;
        }
        if (block.feed) line += `F${Math.round(block.feed)} `;
        lines.push(line.trim());
        continue;
      }
    }

    // Tool change
    if (block.toolNumber !== undefined && !block.heidenhainCommand) {
      line = `TOOL CALL ${block.toolNumber} Z S${Math.round(block.spindleSpeed || 5000)}`;
      lines.push(line);
      continue;
    }

    // Standalone M codes
    if (block.mCodes.length > 0 && !block.heidenhainCommand) {
      lines.push(block.mCodes[0]);
      continue;
    }

    // Comment only
    if (block.comment) {
      lines.push(`;${block.comment}`);
      continue;
    }
  }

  return lines.join("\n");
}


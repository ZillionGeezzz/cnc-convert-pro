import { CNCBlock, CNCProgram, ControllerFormat, ConversionOptions } from "./types";

/**
 * Generate Siemens 840d code from a parsed program
 */
export function generateSiemens840d(program: CNCProgram, options?: ConversionOptions): string {
  const lines: string[] = [];

  for (let i = 0; i < program.blocks.length; i++) {
    const block = program.blocks[i];
    let line = "";

    if (block.isProgramStart) {
      lines.push(`%_N_${program.programName || "PROGRAM"}_MPF`);
      lines.push(`;${options?.commentStyle === "parens" ? "(" : ""} Generated by CNC Converter${options?.commentStyle === "parens" ? ")" : ""}`);
      continue;
    }

    if (block.isProgramEnd) {
      lines.push(block.mCodes.length ? block.mCodes[0] : "M02");
      lines.push("%");
      continue;
    }

    // Build addresses from parsed data
    for (const g of block.gCodes) {
      line += g + " ";
    }

    // Add axes
    for (const axis of ["X", "Y", "Z", "A", "B", "C"]) {
      if (block.axes[axis] !== undefined) {
        line += `${axis}=${formatNumber(block.axes[axis])} `;
      }
    }

    if (block.feed !== undefined) {
      line += `F=${formatNumber(block.feed, 2)} `;
    }

    if (block.spindleSpeed !== undefined) {
      line += `S=${Math.round(block.spindleSpeed)} `;
    }

    if (block.toolNumber !== undefined) {
      line += `T${block.toolNumber} `;
    }
    if (block.toolName !== undefined) {
      line += `T="${block.toolName}" `;
    }

    // Siemens cycles
    if (block.siemensCycleCall) {
      line = block.siemensCycleCall + " ";
    }

    for (const m of block.mCodes) {
      line += m + " ";
    }

    // Add other addresses
    for (const [addr, val] of Object.entries(block.addresses)) {
      if (!["G", "M", "X", "Y", "Z", "A", "B", "C", "F", "S", "T", "I", "J", "K", "R", "D", "H", "P", "Q", "CALL"].includes(addr)) {
        line += `${addr}${typeof val === "number" ? formatNumber(val) : val} `;
      }
    }

    line = line.trim();

    if (block.comment) {
      line += line ? ` ;${block.comment}` : `;${block.comment}`;
    }

    lines.push(line || "");
  }

  return lines.join("\n");
}


export function generateBoschMTX(program: CNCProgram, options?: ConversionOptions): string {
  return generateSiemens840d(program, options);
}

/**
 * Convert Siemens cycle call to Fanuc-style fixed cycle
 */
function convertSiemensCycleToFanuc(block: CNCBlock, format: ControllerFormat): string {
  const cycle = block.cycle?.toUpperCase();
  const params = block.cycleParams;
  let line = "";

  switch (cycle) {
    case "CYCLE81": {
      const rfp = params[1] || 0;
      const sdip = params[2] || 0;
      const dp = params[3] || 0;
      const ret = rfp + sdip;
      line = `G81 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(ret)} F${block.feed || 100}`;
      break;
    }
    case "CYCLE83": {
      const rfp = params[1] || 0;
      const dp = params[3] || 0;
      const fdep = params[5] || dp / 2;
      const dam = params[7] || 3;
      const ret = rfp + (params[2] || 0);
      line = `G83 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(ret)} Q${formatNumber(fdep > 0 ? fdep : Math.min(dam, dp / 4))} F${block.feed || 80}`;
      break;
    }
    case "CYCLE84": {
      const dp = params[3] || 0;
      const rfp = params[1] || 0;
      const pitch = params[8] || 1.5;
      const ret = rfp + (params[2] || 0);
      const sst = params[10] || block.spindleSpeed || 500;
      line = `G84 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(ret)} F${formatNumber(sst * pitch, 2)} S${Math.round(sst)}`;
      break;
    }
    case "CYCLE85": {
      const dp = params[3] || 0;
      const rfp = params[1] || 0;
      const ffr = params[6] || block.feed || 100;
      const ret = rfp + (params[2] || 0);
      line = `G85 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(ret)} F${formatNumber(ffr, 2)}`;
      break;
    }
    default:
      line = `(${block.siemensCycleCall})`;
      break;
  }

  return line;
}

/**
 * Convert Siemens cycle to G81 with depth (simple conversion)
 */
function convertSiemensCycleToG81(block: CNCBlock): string {
  const cycle = block.cycle?.toUpperCase();
  const params = block.cycleParams;
  let line = "";

  switch (cycle) {
    case "CYCLE81": {
      const dp = params[3] || 0;
      line = `G81 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(params[1] || 2)} F${block.feed || 100}`;
      break;
    }
    case "CYCLE83": {
      const dp = params[3] || 0;
      const dam = params[7] || 3;
      line = `G83 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(params[1] || 2)} Q${Math.min(dam, Math.abs(dp) / 4)} F${block.feed || 80}`;
      break;
    }
    case "CYCLE84": {
      const dp = params[3] || 0;
      const pitch = params[8] || 1.5;
      const sst = params[10] || block.spindleSpeed || 500;
      line = `G84 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(params[1] || 2)} F${formatNumber(sst * pitch, 2)} S${Math.round(sst)}`;
      break;
    }
    case "CYCLE85": {
      const dp = params[3] || 0;
      const ffr = params[6] || block.feed || 100;
      line = `G85 X${formatNumber(block.axes["X"] || 0)} Y${formatNumber(block.axes["Y"] || 0)} Z${formatNumber(-dp)} R${formatNumber(params[1] || 2)} F${formatNumber(ffr, 2)}`;
      break;
    }
    default:
      line = `(${block.siemensCycleCall})`;
      break;
  }

  return line;
}

function formatNumber(num: number, decimals?: number): string {
  const val = typeof num === "number" ? num : parseFloat(String(num));
  if (isNaN(val)) return "0";
  if (decimals !== undefined) {
    return val.toFixed(decimals);
  }
  return parseFloat(val.toFixed(4)).toString();
}


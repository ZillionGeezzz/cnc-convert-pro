/**
 * Generate Haas code (Fanuc-style with decimal points)
 */
export function generateHaas(program: CNCProgram, options?: ConversionOptions): string {
  const lines: string[] = [];
  const progNum = options?.programNumber || 1;

  for (let i = 0; i < program.blocks.length; i++) {
    const block = program.blocks[i];
    let line = "";

    if (block.isProgramStart) {
      lines.push(`O${String(progNum).padStart(4, "0")}`);
      lines.push(`(Generated by CNC Converter)`);
      continue;
    }

    if (block.isProgramEnd) {
      if (block.mCodes.length) {
        // Haas uses M30 for program end
        lines.push(block.mCodes.includes("M30") ? "M30" : block.mCodes[0]);
      } else {
        lines.push("M30");
      }
      lines.push("%");
      continue;
    }

    // Siemens cycles → Haas
    if (block.siemensCycleCall) {
      line = convertSiemensCycleToFanuc(block, "haas");
      if (block.comment) line += ` (${block.comment})`;
      lines.push(line);
      continue;
    }

    // Heidenhain → Haas
    if (block.heidenhainCommand === "L") {
      line = "G01 ";
      for (const axis of ["X", "Y", "Z", "A", "B", "C"]) {
        if (block.axes[axis] !== undefined) {
          line += `${axis}${block.axes[axis].toFixed(3)} `;
        }
      }
      if (block.feed) line += `F${block.feed.toFixed(2)} `;
      for (const m of block.mCodes) line += m + " ";
      lines.push(line.trim());
      continue;
    }

    if (block.heidenhainCommand === "TOOL CALL" && block.toolNumber) {
      line = `T${String(block.toolNumber).padStart(2, "0")} M6` + (block.spindleSpeed ? ` S${Math.round(block.spindleSpeed)}` : "");
      lines.push(line);
      continue;
    }

    // Build standard addresses
    for (const g of block.gCodes) {
      const gNum = parseInt(g.replace("G", ""), 10);
      if (!isNaN(gNum)) {
        line += `G${String(gNum).padStart(2, "0")} `;
      } else {
        line += g + " ";
      }
    }

    for (const axis of ["X", "Y", "Z", "A", "B", "C"]) {
      if (block.axes[axis] !== undefined) {
        line += `${axis}${block.axes[axis].toFixed(3)} `;
      }
    }

    if (block.addresses["I"] !== undefined) line += `I${formatNumber(block.addresses["I"] as number)} `;
    if (block.addresses["J"] !== undefined) line += `J${formatNumber(block.addresses["J"] as number)} `;
    if (block.addresses["K"] !== undefined) line += `K${formatNumber(block.addresses["K"] as number)} `;

    if (block.feed !== undefined) line += `F${formatNumber(block.feed, 2)} `;
    if (block.spindleSpeed !== undefined) line += `S${Math.round(block.spindleSpeed)} `;
    if (block.toolNumber !== undefined) line += `T${String(block.toolNumber).padStart(2, "0")} `;
    if (block.addresses["R"] !== undefined) line += `R${formatNumber(block.addresses["R"] as number)} `;
    if (block.addresses["Q"] !== undefined) line += `Q${formatNumber(block.addresses["Q"] as number)} `;

    for (const m of block.mCodes) line += m + " ";

    line = line.trim();
    if (block.comment) line += line ? ` (${block.comment})` : `(${block.comment})`;
    lines.push(line || "");
  }

  return lines.join("\n");
}

